//删除url中参数
function delUrlParam(param) {
    var obj = new window.URL(window.location.href);
    obj.searchParams.delete(param);
    return obj.href;
}
//增加url中的参数
function addUrlParam(key, value) {
    var obj = new window.URL(window.location.href);
    obj.searchParams.set(key, value);
    return obj.href;
}

